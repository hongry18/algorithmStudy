def insertData(head, data):
    cur = head

    while cur:
        if cur.data == data:
            break

        if data < cur.data:
            nd = cur.data
            nn = cur.next
            cur.data = data
            cur.next = SinglyLinkedListNode(nd)
            cur.next.next = nn
            break

        if cur.next is None:
            cur.next = SinglyLinkedListNode(data)
            break

        cur = cur.next

def removeDuplicates(head):
    res = SinglyLinkedListNode(head.data)
    head = head.next

    while head:
        insertData(res, head.data)
        head = head.next

    return res

